export const setRegisterName = data => {
  return {
    type: 'SET_REGISTER_NAME',
    payload: data,
  };
};
export const setRegisterEmail = data => {
  return {
    type: 'SET_REGISTER_EMAIL',
    payload: data,
  };
};

export const setRegisterPassword = data => {
  return {
    type: 'SET_REGISTER_PASSWORD',
    payload: data,
  };
};
